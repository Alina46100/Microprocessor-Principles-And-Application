#INCLUDE <p18f4520.inc>
 CONFIG OSC = INTIO67
 CONFIG WDT = OFF 
 org 0x00 ;PC = 0x00
MOVLW 0x28
MOVWF 0x000
MOVLW 0x34
MOVWF 0x001
MOVLW 0x7A
MOVWF 0x002
MOVLW 0x80
MOVWF 0x003
MOVLW 0xA7
MOVWF 0x004
MOVLW 0xD1
MOVWF 0x005
MOVLW 0xFE
MOVWF 0x006
MOVLW 0x00
MOVWF 0x010
MOVLW 0x07
MOVWF 0x011
 LOOP:
  LFSR 0, 0x000
  DECF 0x011;r-1
  
  RRNCF 0x011;/2
  MOVF 0x011, W;to the
  ADDWF 0x010, W;+l
  MOVWF 0x012;m
   MOVFF 0x012, 0x013
  LOOPFORPOINTER:
  MOVF POSTINC0,W
 
  DECFSZ 0x013
    GOTO LOOPFORPOINTER
  MOVLW 0xFF
  CPFSEQ INDF0
     GOTO NE
  GOTO FIND
 NE:
  CPFSGT INDF0
      GOTO LT
  MOVFF 0x012, 0x011
  DECF 0x011                  

  MOVLW 0x010
  CPFSGT 0x011
   GOTO LOOP
  GOTO NOT
  LT:
    MOVFF 0x012, 0x010
    INCF 0x010
    MOVLW 0x010
    CPFSGT 0x011
	GOTO LOOP
    GOTO NOT
FIND:
MOVLW 0xFF
 MOVWF 0x011
 GOTO ENDEND
NOT:
 MOVLW 0x00
 MOVWF 0x011
ENDEND:
 end


